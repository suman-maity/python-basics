# python is a dynamically typed language
a = 5          # integer
b = 3.14        # float

print(a,b)
print(type(a),type(b))

# convert integer to float
c = float(a)
print("Value:", c, ", Type:", type(c), sep="")

# convert float to integer
d = int(b)
print("Value:", d, ", Type:", type(d), sep="")

# boolean datatype
e = True
f = False
print(e, f, type(e))
print(7>6)